// Function tail which returns the "tail" of an array: everything except for the first item (head) of the provided array

const tail = (arr) => {
  let newArray = [...arr];
  newArray.splice(0, 1);
  return newArray;
};

const result = tail(["Hello", "Lighthouse", "Labs"]);
